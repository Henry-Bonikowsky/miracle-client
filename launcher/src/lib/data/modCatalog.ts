export function getModsByCategory(category: ModCategory): ModCatalogEntry[] {
  if (category === 'all') {
    return MOD_CATALOG;
  }
  return MOD_CATALOG.filter(mod => mod.category === category);
}

export function getRecommendedMods(): ModCatalogEntry[] {
  return MOD_CATALOG.filter(mod => mod.recommended);
}

export function getModById(id: string): ModCatalogEntry | undefined {
  // Try exact match first
  let found = MOD_CATALOG.find(mod => mod.id === id);
  if (found) return found;

  // Try case-insensitive match
  const lowerCaseId = id.toLowerCase();
  found = MOD_CATALOG.find(mod => mod.id.toLowerCase() === lowerCaseId);
  if (found) return found;

  // Try with/without hyphens and underscores
  const normalizedId = lowerCaseId.replace(/[-_]/g, '');
  found = MOD_CATALOG.find(mod => mod.id.toLowerCase().replace(/[-_]/g, '') === normalizedId);
  if (found) return found;

  // Try matching by name (case-insensitive, no special chars)
  const normalizedName = id.toLowerCase().replace(/[^a-z0-9]/g, '');
  found = MOD_CATALOG.find(mod => {
    const catalogName = mod.name.toLowerCase().replace(/[^a-z0-9]/g, '');
    return catalogName === normalizedName;
  });
  if (found) return found;

  // Try matching by slug
  found = MOD_CATALOG.find(mod => mod.slug.toLowerCase() === lowerCaseId);
  if (found) return found;

  const normalizedSlug = lowerCaseId.replace(/[-_]/g, '');
  found = MOD_CATALOG.find(mod => mod.slug.toLowerCase().replace(/[-_]/g, '') === normalizedSlug);

  return found;
}
